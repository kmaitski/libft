#include "libft.h"

/* 
 * ===  FUNCTION  ==============================================================
 *         Name:  ft_isalnum
 *  Description:  Checks if the passed character is alphanumeric.
 * =============================================================================
 */
int	ft_isalnum(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') ||
			(c >= '0' && c <= '9'))
		return (1);
	else
		return (0);
}		/* -----  end of function ft_isalnum  ----- */
