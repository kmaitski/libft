#include "libft.h"

/* 
 * ===  FUNCTION  ==============================================================
 *         Name:  ft_putstr
 *  Description:  Outputs the string s to the standard output. 
 * =============================================================================
 */
void	ft_putstr(char const *s)
{
	if (!s)
		return ;
	while (*s)
		ft_putchar(*s++);
}
