#include "libft.h"

/* 
 * ===  FUNCTION  ==============================================================
 *         Name:  ft_putchar_fd
 *  Description:  Outputs the char c to the file descriptor fd.
 * =============================================================================
 */
void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}		/* -----  end of function ft_putchar_fd  ----- */
