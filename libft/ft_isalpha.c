#include "libft.h"

/* 
 * ===  FUNCTION  ==============================================================
 *         Name:  ft_isalpha
 *  Description:  Checks if the passed character is alphabetic.
 * =============================================================================
 */
int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}		/* -----  end of function ft_isalpha  ----- */
