#include "libft.h"

/* 
 * ===  FUNCTION  ==============================================================
 *         Name:  ft_strclr
 *  Description:  Sets every character of the string to the value ’\0’.
 * =============================================================================
 */
void	ft_strclr(char *s)
{
	if (!s)
		return ;
	while (*s)
		*s++ = '\0';
}	  /* -----  end of function ft_strclr  ----- */
