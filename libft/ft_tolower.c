#include "libft.h"

/* 
 * ===  FUNCTION  ==============================================================
 *         Name:  ft_tolower
 *  Description:  Converts a given letter to lowercase.
 * =============================================================================
 */
int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		c = c + 32;
	return (c);
}		/* -----  end of function ft_tolower  ----- */
