#include "libft.h"

/* 
 * ===  FUNCTION  ==============================================================
 *         Name:  ft_putstr_fd
 *  Description:  Outputs the string s to the file descriptor fd.
 * =============================================================================
 */
void	ft_putstr_fd(char const *s, int fd)
{
	if (!s)
		return ;
	while (*s)
		ft_putchar_fd(*s++, fd);
}		/* -----  end of function ft_putstr_fd  ----- */
