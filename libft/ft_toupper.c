#include "libft.h"

/* 
 * ===  FUNCTION  ==============================================================
 *         Name:  ft_toupper
 *  Description:  Converts lowercase letter to uppercase.
 * =============================================================================
 */
int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		c = c - 32;
	return (c);
}		/* -----  end of function ft_toupper  ----- */
