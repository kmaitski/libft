#include "libft.h"

char	*ft_epur_str(char *str)
{
	unsigned int	i = 0;

	while (str[i] == ' ' || str[i] == '	')
		i++;
	while (str[i])
	{
		if (str[i] > 32 || (str[i] < 33 && str[i + 1] > 32))
			write(1, &str[i], 1);
		i++;
	}
	return (str);
}
