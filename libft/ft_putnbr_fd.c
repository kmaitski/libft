#include "libft.h"

/* 
 * ===  FUNCTION  ==============================================================
 *         Name:  ft_putnbr_fd
 *  Description:  Outputs the integer n to the file descriptor fd.
 * =============================================================================
 */
void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	if (n < 10)
	{
		ft_putchar_fd(n + '0', fd);
		return ;
	}
	ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd((n % 10) + '0', fd);
}		/* -----  end of function ft_putnbr_fd  ----- */
